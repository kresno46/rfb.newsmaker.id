                  <h1>Trump orders 100% tariff on foreign-made movies to save 'dying' Hollywood</h1>
                  <p><br>U.S. President Donald Trump on Sunday announced a 100% tariff on movies produced outside the country, saying the American movie industry was dying a "very fast death" due to the incentives that other countries were offering to lure filmmakers.
                </p>

                <p><br>"This is a concerted effort by other Nations and, therefore, a National Security threat. It is, in addition to everything else, messaging and propaganda," Trump said on Truth Social.
                </p>

                <p><br>Trump said he was authorizing the relevant government agencies, such as the Department of Commerce, to immediately begin the process of imposing a 100% tariff on all films produced abroad that are then sent into the United States.
                </p>

                <p><br>He added: "WE WANT MOVIES MADE IN AMERICA, AGAIN!"
                </p>

                <p><br>Commerce Secretary Howard Lutnick said on X: "We're on it."
                </p>

                <p><br>Neither Lutnick nor Trump provided any details on how the tariffs would be implemented.
                </p>

                <p><br>It was unclear if the tariffs would apply to movies on streaming services as well as those shown in theaters, or if they would be calculated based on production costs or box office revenue. Hollywood executives were trying to sort out details on Sunday night. The Motion Picture Association, which represents the major studios, had no immediate comment.
                    In January, Trump appointed Hollywood veterans Jon Voight, Sylvester Stallone and Mel Gibson to bring Hollywood back "bigger, better and stronger than ever before."
                    Movie and TV production has been exiting Hollywood for years, heading to locations with tax incentives that make filming cheaper.
                </p>

                <p><br>Governments around the world have increased credits and cash rebates to attract productions and capture a greater share of the $248 billion that Ampere Analysis predicts will be spent globally in 2025 to produce content.
                </p>

                <p><br>All major media companies, including Walt Disney (NYSE:DIS), Netflix (NASDAQ:NFLX) and Universal Pictures, film overseas in countries such as Canada and Britain.
                </p>

                <p><br>Shares in Disney, Warner Bros Discovery (NASDAQ:WBD), Paramount Global and Amazon.com (NASDAQ:AMZN) were lower in early trade on Monday.
                </p>

                <p><br>On Monday, leaders in Australia and New Zealand responded to Trump's tariff announcement by saying they would advocate for their local industries. Some Marvel superhero movies have been filmed in Australia, while New Zealand was the backdrop for "The Lord of the Rings" films.
                </p>

                <p><br>The British media and entertainment union Bectu called on the government to move swiftly to protect the country's "vital" film industry.
                </p>

                <p><br>"These tariffs, coming after COVID and the recent slowdown, could deal a knock-out blow to an industry that is only just recovering," said Bectu head Philippa Childs.
                </p>

                <p><br>She said they would also threaten tens of thousands of jobs of freelancers who make films in the UK.
                </p>

                <p><br>Film and high-end TV production in the UK was worth 5.6 billion pounds ($7.45 billion) in 2024, a 31% increase over 2023, according to the British Film Institute.
                    The government's Department for Culture, Media, and Sport (DCMS) did not immediately respond to a Reuters request for comment.
                </p>

                <p><br>'LOT MORE TO LOSE THAN TO GAIN'
                </p>

                <p><br>In 2023, about half of the spending by U.S. producers on movie and TV projects with budgets of more than $40 million went outside the U.S., according to research firm ProdPro.
                </p>

                <p><br>"I think the most likely consequence is not that more productions will be done outside of America, or more productions would be done in America, but probably that just fewer productions will (be made)," said Scott Roxborough, Europe bureau chief of the Hollywood Reporter.
                </p>

                <p><br>Film and television production has fallen by nearly 40% over the last decade in Hollywood's home city of Los Angeles, according to FilmLA, a non-profit that tracks the region's production.
                </p>

                <p><br>The January wildfires accelerated concerns that producers may look outside Los Angeles, and that camera operators, costume designers, sound technicians and other behind-the-scenes workers may move out of town rather than try to rebuild in their neighborhoods.
                </p>

                <p><br>A ProdPro survey of executives found California was the sixth most preferred place to film in the next two years, behind Toronto, Britain, Vancouver, Central Europe and Australia.
                </p>

                <p><br>Hollywood producers and labor unions have been urging Governor Gavin Newsom to boost the state's tax incentives to better compete with other locations.
                </p>

                <p><br>Trump's proposed movie tariff follows a series of trade conflicts initiated by his administration, which have roiled markets and led to fears of a U.S. recession.
                    Former senior Commerce official William Reinsch, a senior fellow with the Center for Strategic and International Studies, said retaliation against Trump's film tariffs would be devastating.
                    "The retaliation will kill our industry. We have a lot more to lose than to gain," he said, adding it would be difficult to make a national security or national emergency case for movies.
                </p>

                <p><br>Source: Investing.com</p>

